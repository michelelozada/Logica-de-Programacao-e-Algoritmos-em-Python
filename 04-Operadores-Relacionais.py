# 1 - Igualdade (==)
print(4 == 4.0)  # Retorna: True


# 2 - Maior do que (>)
print(5 > 3) # True


# 3 - Menor do que (<)
print(2.5 < 10) # True


# 4 - Maior ou igual (>=)
print (5 >= 13) # False
print(5.0 >= 5) #True


# 5 - Menor ou igual  (<=)
print (10 <= 5) # False
print(10 <= 10.0) # True


# 6 - Comparando strings
print('bom' < 'dia') # True
print('bom' == 'dia') # False
print('bom' > 'dia') # False
print('dia' == 'dia') # True